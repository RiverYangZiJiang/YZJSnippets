默认行为：
1.如果UIScrollView上的子控件不管是在横的方向上还是垂直方向上超过屏幕，拖动其上的子控件，就可以横向或垂直方向滚动了；如果没有超过屏幕，则不可以滚动

使用场景：
在横向或垂直方向可能会超过屏幕，并需要滚动的地方，此时会向其添加多个子控件